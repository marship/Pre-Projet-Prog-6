package Modele;

public class Position {

    public int positionX;
    public int positionY;

    Position(int posX, int posY) {
        positionX = posX;
        positionY = posY;
    }

    public int positionX() {
        return positionX;
    }

    public int positionY() {
        return positionY;
    }
}
